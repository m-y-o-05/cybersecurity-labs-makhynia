/*
 * Main.cpp.
 *
 * Головний файл програми.
 * Точка входу в програму "Аналізатор безпеки паролів".
 */

#include "MainWindow.h"
#include <QApplication>
#include <QLocale>

/**
 * @brief Головна функція програми.
 * @param argc Кількість аргументів командного рядка.
 * @param argv Масив аргументів командного рядка.
 * @return Код завершення програми (0 = успіх, інше = помилка).
 *
 * Ця функція виконує наступні дії:
 *
 * 1. Створення об'єкта QApplication:
 *    - Ініціалізує систему Qt GUI.
 *    - Обробляє аргументи командного рядка.
 *
 * 2. Встановлення локалізації:
 *    - Встановлює українську мову для форматування дат, чисел тощо.
 *    - Забезпечує коректне відображення дат у форматі dd.MM.yyyy.
 *
 * 3. Створення та відображення головного вікна:
 *    - Створює об'єкт MainWindow.
 *    - Викликає show() для відображення вікна.
 *
 * 4. Запуск головного циклу обробки подій:
 *    - app.exec() запускає event loop Qt.
 *    - Програма чекає на події (натискання кнопок, введення тексту).
 *    - Цикл працює до закриття вікна або виклику QApplication::quit().
 *
 * @note Функція блокується на app.exec() до завершення програми.
 */
int main(int argc, char *argv[]) {
    // ============ Створення об'єкта додатку Qt ============
    QApplication app(argc, argv);

    // ============ Встановлення української локалі ============
    // Це впливає на форматування дат, чисел, валют тощо.
    // Використовується в QDateEdit для відображення дати у форматі dd.MM.yyyy.
    QLocale::setDefault(QLocale(QLocale::Ukrainian, QLocale::Ukraine));

    // ============ Створення та відображення головного вікна ============
    MainWindow window;
    window.show();

    // ============ Запуск циклу обробки подій ============
    // Функція exec() блокує виконання програми та обробляє події
    // (натискання кнопок, введення тексту, малювання вікон тощо)
    // до закриття програми користувачем.
    //
    // Повертає код завершення:
    // - 0: нормальне завершення.
    // - інше: аварійне завершення або виклик QApplication::exit(code).
    return app.exec();
}
